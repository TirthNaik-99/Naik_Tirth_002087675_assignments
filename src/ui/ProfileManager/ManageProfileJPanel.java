    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblProfile.getModel();
        model.setRowCount(0);
        
        for (Profile p : profileDirectory.getProfiles()){
            
            Object[] row = new Object[7];
            row[0] = p;
            row[1] = p.getLastName();
            row[2] = p.getSsn();
            row[3] = p.getHomeCity();
            row[4] = String.valueOf(p.getHomeZip());
            row[5] = p.getWorkCity();
            row[6] = String.valueOf(p.getWorkZip());
            
            model.addRow(row);
        }
    }
