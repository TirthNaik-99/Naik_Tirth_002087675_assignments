    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProfile.getSelectedRow();
        
        
        if (selectedRow >= 0){
            Profile selectedProfile = (Profile) tblProfile.getValueAt(selectedRow, 0);
            
            ViewProfileJPanel panel = new ViewProfileJPanel(userProcessContainer, profileDirectory, selectedProfile);
            userProcessContainer.add("ViewProfileJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            
        }else{
            JOptionPane.showMessageDialog(null, "Please select an profile from the list to view", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

